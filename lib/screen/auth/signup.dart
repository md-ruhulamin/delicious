  Future<void> addusersDataToFirestore() async {
    try {
      // Get a reference to the Firestore collection

      CollectionReference products =
          FirebaseFirestore.instance.collection('users');
      String id = DateTime.now().millisecondsSinceEpoch.toString();
      // Add a document with data to the collection
      await products.doc(id).set({
        "id": id,
        "gmail": signupEmailController.text.toString(),
        "name": signupNameController.text.toString(),
        "password": signupPasswordController.text,
      });

      Get.snackbar("User Info", " is Added ",
          backgroundColor: Colors.amber, snackPosition: SnackPosition.TOP);

      print('Data added to Firestore');
    } catch (e) {
      // Handle the error here
      print('Error adding data to Firestore: $e');
    }
