//.onError((error, stackTrace) => CustomSnackBar(context,Text(error.toString());
  Future<void> login() async {
    try {
      _auth
          .signInWithEmailAndPassword(
              email: loginEmailController.text.toString().trim(),
              password: loginPasswordController.text.toString().trim())
          .then((value) {
        CustomSnackBar(
          context,
          Text("Email ${value.user!.email.toString()} ,Login Successful"),
        );

        Navigator.push(
            context, MaterialPageRoute(builder: (context) => HomeWithNavBar()));
      });
    } catch (e) {
      CustomSnackBar(
        context,
        Text("Login Failed $e"),
      );
    }
  }

